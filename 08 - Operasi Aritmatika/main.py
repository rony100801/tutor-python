# OPEARASI ARITMATIKA

x = 10
y = 4
print("\n")
# PENJUMLAHAN (+)
print("===== PENJUMLAHAN =====")
hasil = x + y
print(x, " + ", y, " = ", hasil)

# PENGURANGAN (-)
print("===== PENGURANGAN =====")
hasil = x - y
print(x, " - ", y, " = ", hasil)

# PERKALIAN (*)
print("===== PERKALIAN =====")
hasil = x * y
print(x, " * ", y, " = ", hasil)

# PEMBAGIAN (/)
print("===== PEMBAGIAN =====")
hasil = x / y
print(x, " / ", y, " = ", hasil)

# EKSPONEN atau Pangkat (**)
print("===== EKSPONEN atau Pangkat =====")
hasil = x ** y
print(x, " ** ", y, " = ", hasil)

# MODULUS (%)
print("===== MODULUS =====")
hasil = x % y
print(x, " % ", y, " = ", hasil)

# FLOOR DIVISION (//)
print("===== FLOOR DIVISION =====")
hasil = x // y
print(x, " // ", y, " = ", hasil)

# PRIORITAS OPERASI
print("===== PRIORITAS OPERASI =====")
# () Kurung
# Eksponen
# Perkalian
# Penjumlahan
a = 3
b = 5
c = 8

hasil = (a + b) * c
print("(", a, " + ", b, ") *", c, " = ", hasil)

print("\n")
